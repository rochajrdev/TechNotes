namespace HelloWorld; // namespace é um bloco de código que contém classes

class program // classe é um bloco de código que contém métodos e variáveis
{
    static void Main() //coloco void porque não quero retornar nada
    {
        Console.WriteLine("Hello, World!"); // Console é uma classe que tem um método WriteLine
                                            // metodo WriteLine escreve uma linha de texto no console
        Console.WriteLine("My name is Rocha!"); // metodo é uma função que pertence a uma classe

    }
}